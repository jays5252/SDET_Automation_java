package org.basic.java;


public class AirthMeticMethods 
{
	public int Sum(int a, int b)
	{
		int sum = a+b;
		return sum;
	}
	
	public int Sub(int a, int b)
	{
		int sub = a-b;
		return sub;
	}
	public int Multi(int a, int b)
	{
		int multi = a*b;
		return multi;
		
	}
	public int Div(int a, int b)
	{
		int div = a/b;
		return div;
	}

}
